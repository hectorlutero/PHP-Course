<?php


$num1 = 2;
$num2 = 3;

echo $num1 + $num2;     // PLUS TO SUM 

echo $num1 - $num2;     // MINUS TO SUBTRACT

echo $num1 / $num2;     // SLASH TO DIVIDE

echo $num1 * $num2;     // ASTERISK TO MULTIPLY

echo $num1 ** $num2;    // DOUBLE ASTERISK TO POWER

echo $num1 % $num2;     // MODULE TO FIND ODDS AND EVEN

echo "1" + "1";         // THAT'S NOT A CONCATONATOR, YOU'LL GET A SUM 

echo TRUE + TRUE;       // TRUE IS EQUALS TO 1

echo FALSE + FALSE;     // FALSE IS EQUALS TO 0

